    private void GuardarInicioBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarInicioBtnActionPerformed
        // TODO add your handling code here:
        Alumno alum = new Alumno();
        alum.setApellidoP(this.ApellidoPTxt.getText());
        alum.setApellidoM(this.ApellidoMTxt.getText());
        alum.setNombre(this.NombreTxt.getText());
        alum.setMatricula(Integer.parseInt(this.MatriculaTxt.getText()));
        alum.setCarrera(this.CarreraTxt.getText());

        Nodo nuevo = new Nodo(alum);
        this.Lista.saveInicio(nuevo);
        this.ApellidoPTxt.setText("");
        this.ApellidoMTxt.setText("");
        this.NombreTxt.setText("");
        this.MatriculaTxt.setText("");
        this.CarreraTxt.setText("");
        this.ListaTxt.setText(this.Lista.imprimirListaInicio());
    }//GEN-LAST:event_GuardarInicioBtnActionPerformed

